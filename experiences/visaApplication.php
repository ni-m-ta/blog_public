                        <p>これがまためんどくさいです。まずこちらの<a rel="https://ceac.state.gov/genniv/">サイト</a>にアクセスします。沢山の質問に答えたり、顔写真をアップロードするのに半日ほどかかるかと思います。
                            <br>面接を希望する地域を選択し認証コードを入力したのちに「START AN APPLICATION」を選択。
                        </p>
